import 'package:flutter/painting.dart';

/// Convenience class to access application colors.
abstract class AppColors {
  /// Dark background color.
  static const Color redColor = Color(0xFFD3231E);

  /// Slightly lighter version of [backgroundColor].
  static const Color yellowColor = Color(0xFFF1A22C);

  /// Color used for cards and surfaces.
  static const Color blackColor = Color(0xFF1F2426);

  /// Accent color used in the application.
  static const Color blueColor = Color(0xFF2A87BB);
}
